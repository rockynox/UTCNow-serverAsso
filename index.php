					<h3>UTC Now</h3>
					<p>UTC Now, est une application mobile qui centralise tous les évènements et activités qui ont lieu à l’UTC. Il s’agit d’un agenda partagé et mis à jour en temps réel par l’ensemble des individus impliqués dans l’UTC, avec des vues personnalisées en fonction des préférences de l’utilisateur. Il est possible de recevoir des notifications correspondant à ses points d’intérêt.<br><br>
					L’application regroupe des informations telles que:<br>
						- Les événements associatifs<br>
						- Les réunions d’information et présentations administratives<br>
						- Les cours par salle et étudiants qui s’y trouvent<br>
						- Calendrier scolaire, alternance des semaines, etc...</p>
